    protected $signature = 'csv:scrape';

    protected $description = 'Scrapes CSV files from Rdatasets and stores in SQLite DB';

    public function handle()
    {
        $links = $this->csvLinks();

        if (empty($links)) {
            $this->error('No CSV links found.');
            return;
        }

        $downloadPath = storage_path('app/public/csvs');
        if (!is_dir($downloadPath)) {
            mkdir($downloadPath, 0755, true);
        }

        foreach ($links as $link) {

            /*
                https://vincentarelbundock.github.io/Rdatasets/csv/AER/Affairs.csv
            */
            $tableName = str_replace('https://vincentarelbundock.github.io/Rdatasets/csv/', '', $link);
            $tableName = str_replace('.csv', '', $tableName);
            $tableName = str_replace('/', '_', $tableName);
            $tableName = str_replace('.', '_', $tableName);
            $tableName = strtolower($tableName);

            $filePath = $downloadPath .'/'.$tableName;

            if (!file_exists($filePath)) {
                $this->info("Downloading {$tableName}...");

                $csvData = Http::get($link);
                if (!$csvData->successful()) {
                    $this->error("Failed to download {$tableName}");
                    continue;
                }

                file_put_contents($filePath, $csvData->body());
            }

            if (DB::connection('csvs')->getSchemaBuilder()->hasTable($tableName)) {
                $this->warn("{$tableName} already found, skipping.");
                continue;
            }

            $handle = fopen($filePath, 'r');
            $headers = fgetcsv($handle);
            $firstRow = fgetcsv($handle);

            $dateColumn = null;
            foreach ($firstRow as $index => $value) {
                if ($this->isDate($value)) {
                    $dateColumn = $headers[$index];
                    break;
                }
            }

            $dateColumn = str_replace([' ', '.'], '_', $dateColumn);

            if (!$dateColumn) {
                $this->warn("No date column found in {$tableName}, skipping.");
                fclose($handle);
                continue;
            }

            
            $columns = collect($headers)->map(fn($col) => str_replace([' ', '.'], ['_','_'], trim($col)));


            //dd($columns, $firstRow, $dateColumn);

            try {
                if (!DB::connection('csvs')->getSchemaBuilder()->hasTable($tableName)) {
                    DB::connection('csvs')->getSchemaBuilder()->create($tableName, function ($table) use ($columns, $dateColumn) {
                        foreach ($columns as $index => $column) {
                            
                            if ($column === str_replace([' ','.'], '_', $dateColumn)) {
                                $table->date($column)->nullable();
                            } else {
                                $table->string($column)->nullable();
                            }
                        }
                        $table->string('hype_month')->nullable()->index();
                    });
                }
            } catch (\Exception $e) {
                dd($columns, $tableName, $e->getMessage());
            }

            rewind($handle);
            fgetcsv($handle); // skip header

            try {
                while (($row = fgetcsv($handle)) !== false) {
                    if (count($columns) != count($row)) {
                        continue;
                    }


                    $rowData = array_combine($columns->toArray(), $row);



                    $dateValue = $rowData[str_replace(' ', '_', $dateColumn)] ?? null;

                    if ($dateValue && $this->isDate($dateValue)) {
                        $month = Carbon::parse($dateValue)->format('Y-m');
                        $rowData['hype_month'] = $month;
                        $rowData[str_replace(' ', '_', $dateColumn)] = Carbon::parse($dateValue)->toDateString();
                    } else {
                        $rowData['hype_month'] = null;
                        $rowData[str_replace(' ', '_', $dateColumn)] = null;
                    }

                    DB::connection('csvs')->table($tableName)->insert($rowData);
                }
            } catch (\Exception $e) {
                dd($rowData, $tableName, $e->getMessage());
            }

            fclose($handle);
            $this->info("Saved {$tableName} and data imported to database.");
        }

        $this->info('All CSV files processed.');
    }

    private function isDate($value)
    {
        if (is_numeric($value)) {
            return false;
        }
        try {
            $date = Carbon::parse($value);
            return $date->year && $date->month && strlen(trim($value)) > 5 && preg_match('/\b\d{4}\b/', $value);
        } catch (\Exception $e) {
            return false;
        }
    }

